# draw a square by turtle.
# left(degree) is used for turning face by angle degree
# same as for right()
import turtle
turtle.getscreen()
turtle.shape("turtle")
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.exitonclick()